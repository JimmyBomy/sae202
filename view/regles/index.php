        <li>Restez en groupe : se séparer fait perdre un temps précieux.</li>
        <li>Manipulez les décors avec soin, ne forcez jamais un mécanisme.</li>
        <li>Un <strong>indice</strong> peut être demandé à tout moment via le talkie fourni.</li>
      </ul>
    </div>

    <div class="carte">

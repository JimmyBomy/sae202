        <li>Sorties de secours signalées et accessibles en permanence.</li>
        <li>Un comédien-maître du jeu vous surveille discrètement tout du long.</li>
        <li>Interdit aux personnes sous l'emprise d'alcool ou de stupéfiants.</li>
      </ul>
    </div>

    <div class="carte">
